    void insert(){
        NguoiDung nv = getForm();
        String mk2 = new String(txtxacnhanmk.getPassword());
        if(mk2.equals(nv.getMatKhau())){
            try{
                dao.insert(nv);// sử dụng câu lệnh insert dao nhân viên để thêm vào csdl
                this.fillTable();// đổ nhân viên vừa thêm vào Jtable
                this.clearForm();// khi thêm xong và xóa trắng các dòng
                MsgBox.alert(this,"Thêm Mới Thành Công ");// đưa ra thông báo
            }catch(Exception e){
                MsgBox.alert(this, "Thêm Mới Thành Công");
            }
        }else{
            MsgBox.alert(this, "Xác Nhận Mật Khẩu Đúng");
        }
    }
    
    void update(){
        NguoiDung nv = getForm();
        String mk2 = new String(txtxacnhanmk.getPassword());
        if(mk2.equals(nv.getMatKhau())){
            try{
                dao.update(nv);// sử dụng câu lệnh update để sửa 1 nhân viên hoặc trưởng phòng từ csdl
                this.fillTable();// đổ nhân viên vừa thêm vào Jtable
                this.clearForm();//khi sửa xong và xóa trắng các dòng
                MsgBox.alert(this,"Cập Nhật Thành Công");
            }catch(Exception e){
                MsgBox.alert(this, "Cập Nhật Thành Công");// sử dụng lớp tiện ích mgsbox.aleert để đưa ra thông báo 
            }
        }else{
            MsgBox.alert(this, "Xác Nhận Mật Khẩu Không Đúng");
        }
    }
    void delete(){
        if(!Auth.isManager()){
            MsgBox.alert(this, "Bạn không có quyền xóa nhân viên!");
        }
        else{
            String manv = txtMaND.getText();
            if(manv.equals(Auth.user.getMaND())){
                MsgBox.alert(this, "Bạn không được xóa chính bạn!");
            }
            else if(MsgBox.confirm(this, "Bạn thực sự muốn xóa nhân viên này?")){
                try {
                    dao.delete(manv);// sử dụng câu lệnh delete để xóa 1 nhân viên hoặc trưởng phòng từ csdl
                    this.fillTable();//xóa nhân viên vừa xóa trong Jtable
                    this.clearForm();//xóa trắng các dòng trong bảng cập nhật
                    DialogHelper.alert(this, "Xóa thành công!");// sử dụng lớp tiện ích mgsbox.aleert để đưa ra thông báo 
                } 
                catch (Exception e) {
                    DialogHelper.alert(this, "Xóa thành công!");
                    //MsgBox.alert(this, "Xóa thành công!");
                }
            }
        }
    }
