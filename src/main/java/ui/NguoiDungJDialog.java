    void fillTable(){
        DefaultTableModel model = (DefaultTableModel)tblnd.getModel();
        model.setRowCount(0);//Xoa tat ca hang trong jtable
        try {
            List<NguoiDung> list = dao.selectAll();// đọc dữ liệu từ csdl 
            for (NguoiDung nv : list) {
            Object[] row = {nv.getMaND(),nv.getMatKhau(),nv.getHoTen(),
            nv.getVaiTro()?"Quản Lý":"Thủ Thư"// trưởng phòng, nhân viên đang đăng nhập 
            };
                model.addRow(row);//thêm 1 hàng vào jtable
            }
            }
         catch(Exception e){
             DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
         }
    }
     NguoiDung getForm(){
        NguoiDung nv = new NguoiDung();
        nv.setMaND(txtMaND.getText());
        nv.setMatKhau(new String(txtMatKhau.getPassword()));
        nv.setHoTen(txtHoTen.getText());
        nv.setVaiTro(rdoQuanLy.isSelected());
        return nv;  
    }
    void clearForm(){// khi chúng ta nhấn mới thì sẽ trả về trạng thái fomr ban đầu
        NguoiDung nv = new NguoiDung();
        this.setForm(nv);  
        this.row = -1;
        this.updateStatus();
    }
    void setForm(NguoiDung nv){
        txtMaND.setText(nv.getMaND());
        txtMatKhau.setText(nv.getMatKhau());
        txtHoTen.setText(nv.getHoTen());
        rdoQuanLy.setSelected(nv.getVaiTro());
        rdoThuThu.setSelected(!nv.getVaiTro());
    }
    void edit(){// 
        try{
        String manv = (String)tblnd.getValueAt(this.row,0);
        NguoiDung nv = dao.selectById(manv);// sử dụng câu lệnh slectByid để chọn từng nhân viên trong Jtable
        if(nv != null){
            setForm(nv);
            //tabs.setSelectedIndex(0);// khi pressed vào bảng thì chuyển qua bảng cập nhật
         }
        }catch (Exception e){
                MsgBox.alert(this,"Lỗi Truy Vấn Dữ Liệu");// đưa ra thông báo lỗi truy vấn dữ liệu
          }
        this.updateStatus();// trả về trạng thái form như ban đầu 
    }

